<h2 class="bold center">A Letter Assignment</h2>
<h3>Instructions</h3>
	<p>
		To complete this assignment, you must first identify a recent (i.e. published within the last two years) peer-reviewed article that presents original research related to the class you are taking.
		Once you've identified a potential article, please email me to make sure the article is acceptable and hasn't already been chosen by another student.
		Typical reasons articles are rejected include not being recent (e.g. more than 2 years old), not being relevant (e.g. a developmental article not exploring cognition for Cognitive Psychology), and not being methodologically appropriate for the class (e.g. a correlational study or meta-analysis instead of an experiment).
		Once you get the green light from me, you're good to go.
	</p>
	<p>
		With an appropriate article in hand, you'll need to write a letter to a friend or family member that is not college educated and familiar with psychology.
		One of the main purposes of this assignment is to help you learn to communicate academic ideas in easily comprehensible ways.
		By writing to someone who isn't college educated and familiar with psychology, you'll be forced to break down the article to its essence to succeed.
		I strongly encourage you to start off the letter with something like "Dear Mom, I miss you so much that I thought about you when reading this article."
		As corny as it sounds, starting off the letter like this should help instantiate the appropriate context for succeeding in this assignment.
		In the letter you need to cover the following points.
			<ol>
				<li>The topic of the article. For articles discussing multiple topics, pick the most important one!</li>
				<li>Why the topic is important to study.</li>
				<li>How the topic was investigated. If the article presents multiple experiments, pick one!</li>
				<li>What the authors learned (i.e. the results of the study and the conclusions the data support).</li>
			</ol>
	</p>
	<p>
		Your letter should not be more than one single-spaced page in length.
		Because of these space limitations, you need to focus in on the most important aspects of your article.
		Most methods sections contain tons of details that are theoretically unimportant.
		What you need to do is ignore what's unimportant in how the authors investigated the topic and focus on what is important.
		Imagine a paper studying the <a href="http://www.apa.org/science/resources/stroop.aspx">Stroop Effect</a>.
		Although the article might mention the size of the letters, the font family that was used, and the particular RGB values of the colors used, these details are probably not very important to the main point the authors intend to make.
		So, make sure you separate the wheat from the chaff.
	</p>
	<p>
		Also, because you are writing a letter to a friend or family member without your expertise and sophistication, you need to be very careful about the words you use.
		<strong>Avoid jargon like the plague!</strong>
		If you must use jargon, make sure you define it.
		For example, instead of saying "there was a stimulus onset asynchrony of fifteen seconds between the conditional and unconditional stimuli" you could say "fifteen seconds after the light turned on, the rat got shocked."
	</p>
	<p>
		Finally, make sure you include an APA reference for the article.
		I suggest you do this in the form of a post script (i.e. P.S.).
	</p>
	<p>
		Once you've completed the assignment, send me an electronic copy over email.
		You will receive feedback and your score within one week of submission.
		The maximum score for this assignment is 100 points.
		Late assignments will be penalized 10% each day that they are late.
		Students who are found guilty of academic integrity violations (e.g. plagiarism) will receive a 0 for the assignment.
	</p>
	<p>
		When submitting papers to journals for publication the authors receive a decision from the journal's editor regarding their decision.
		These decisions usually take one of three forms: acceptance, revise and resubmit, and reject.
		If you get the maximum score before any penalties are assessed, I will accept your paper.
		If you get a score of 30 or less before any penalties and it appears you did not seriously attempt the paper, I will reject it.
		Papers will also be rejected for academic integrity violations (e.g. plagiarism).
		Otherwise, you will be given the opportunity to revise and resubmit your paper.
		Flat out rejections are rare, and normally only occur when the student fails to seriously attempt the paper.
		Revision is an important part of the academic writing process, so I encourage you to take the opportunity to improve your paper based on my feedback.
		Revisions must be submitted before the last regular exam.
		Late revisions will not be accepted.
		Your final grade on the assignment will reflect the points you received for your most recent submission and any late penalties you received for the original submission.
		For example, if you get a 90 on the final submission, but originally submitted the assignment one day late, you would receive an 81.
		You may revise and resubmit your paper as many times as you are able to before the last regular exam.
		You will receive feedback and your score within one week of resubmission.
	</p>
<h3>Purpose</h3>
	<p>
		The main point of this assignment is to help you learn how to communicate psychological science in a way that is easily understood by other non-psychologists.
		Additionally, this assignment will help you improve your writing, analytic, and article-reading abilities.
	</p>
