<p><h1>Teaching Philosophy</h1></p>
	<p>
	My teaching style could best be described as evolving from a solid foundation.
	My style is evolving partially because I'm relatively new to teaching and partially
	because I believe that I can continually improve my teaching effectiveness.  Based
	on student performance, comments, evaluations, intuition, and psychological research,
	I have made significant changes that are helping my students to perform better.
	Despite changes to my style, the six principles upon which I base my teaching style
	are constant.
	</p>
<p><h1>1. Psychology is science</h1></p>
	<p>
	The most common misperception that I encounter is that Psychology is an art rather
	than a science.  In all of my courses I focus on the questions that psychologists ask,
	the testable hypotheses they derive from these questions, the methods they use to test
	these hypotheses, and the results that support one conclusion over another.  I ask my
	students about their beliefs (e.g. are men better drivers than women) and then have
	them create their own methods to test their hypotheses.  This focus on science, with
	a special emphasis on hypothesis testing, is intended to help all of my students become
	better consumers of the information they encounter and to prepare interested students
	for graduate school.
	</p>
<p><h1>2. Respect</h1></p>
	<p>
	I respect my students' scholarly abilities and give them challenging study guides and
	readings, including peer-reviewed journal articles, to complete before class.  I do
	not expect them to comprehend everything in their assignments, but I do expect them
	to try.  Having made the attempt, I expect them to explain their understandings of
	the issues.  Based on their levels of understanding, we approach the day's topics and
	devote more time to those issues that the students found most troublesome.
	</p>
	<p>
	I also respect my students as human beings who have innate value.  Students have busy
	lives and too often can't make it to classes or exams for legitimate reasons.  My
	grading policies allow my students the freedom to take care of their duties, familial,
	or otherwise, without having to divulge information that they would rather keep private.
	To accomplish this I give my students a few free absences and the opportunity to replace
	a missed exam with an optional final.  To avoid speed-accuracy tradeoffs, I also allow
	my students as much time as they need to complete my exams.
	</p>
<p><h1>3. Improving understanding using psychological principles</h1></p>
	<p>
	Students perform better when they distribute their study time (Balch, 2006).  By
	providing study guides, students who attend class and prepare their guides should
	have at least four exposures to the material (i.e. preparation, class, study, and
	the exam).  Using quizzes provides students with additional exposures to the material
	and encourages them to prepare before class.  By providing students with immediate
	feedback on their quizzes and exams, my assessments also serve as additional learning
	tools which facilitate retention (Butler & Roediger, 2008).  My study guides are
	designed to be challenging, which should lead to deeper encoding and better performance
	(Craik & Lockhart, 1972).  Furthermore, the exams are designed from the study guides
	to further improve performance through transfer appropriate processing (Morris,
	Bransford, & Franks, 1977).
	</p>
<p><h1>4. Communicating clearly</h1></p>
	<p>
	My ability to communicate is one of my most important abilities as an educator.  To
	enhance classroom learning, I employ a number of methods to facilitate communicating
	with my students outside of the classroom.  During office hours and at other times,
	I am logged onto AOL Instant Messenger, Google Chat, Yahoo Messenger, and Windows Live
	ready to help students with any problems they might have.  I provide my students with
	my home phone number and encourage them to use it before 10:00 p.m., unless they have
	an emergency.  Finally, I provide students with an anonymous method to email me.  One
	of my pet peeves in higher education is that instructors usually don't get feedback
	about their courses until it's too late to benefit those who provided the feedback.
	My anonymous email form allows my students to impact their own education.
	</p>
	<p>
	A student's ability to communicate is one of the most important abilities they can
	develop regardless of their chosen field.  Because of this, I require writing assignments
	in all of my upper-level courses.  My favorite writing assignment involves summarizing
	a peer-reviewed journal article in a way that a high school student could understand.
	This exercise requires that students extract the essence of the article and communicate
	it in a jargon-free way.  This exercise helps them develop their critical thinking and
	verbal communications skills.
	</p>
<p><h1>5. Revision</h1></p>
	<p>
	In graduate school, I learned the importance of revision.  My evolving teaching style
	reflects my approach to improving my teaching effectiveness through revision.  Beyond
	my continuing attempts to provide my students with more value, I stress the importance
	of revision in all of my upper-level courses.  I encourage my students to revise and
	resubmit their writing assignments as many times as they like before the end of the
	semester.  Although students appear to do this primarily to increase their grades, both
	my students and I have noticed how much their writing improves with revision. 
	</p>
<p><h1>6. Fun</h1></p>
	<p>
	People perform better when they are happy and enjoy what they do (Wright, Cropanzano,
	& Bonett, 2007).  Because having fun makes me happy and helps me enjoy my work, I try
	to have as much fun as I can when I teach.  I am enthusiastic about what I teach, so
	this isn't usually too difficult.  Many of the revisions to my teaching methods increased
	the amount of fun that my students and I were having.  I use real-world analogies and
	popular culture to connect abstract theory to concrete examples.  My intention is both
	to grab my students' attention so they learn better and, hopefully, to inspire the next
	generation of psychologists.
	</p>
